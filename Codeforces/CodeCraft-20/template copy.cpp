template<typename T>
void printVec(vector<T> v) {
    int n = v.size();
    for(int i = 0; i < n; ++i) {
        cout << v[i] << ' ';
    }
    cout << '\n';
}

template<typename T>
void printArr(T arr[], int n) {
    for(int i = 0; i < n; ++i) {
        cout << arr[i] << ' ';
    }
    cout << '\n';
}

